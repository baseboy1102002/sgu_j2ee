package model;

public class TimeDifference {
	private String timeDiff;

	public TimeDifference() {
		super();
	}
	public TimeDifference(String timeDiff) {
		super();
		this.timeDiff = timeDiff;
	}

	public String getTimeDiff() {
		return timeDiff;
	}

	public void setTimeDiff(String timeDiff) {
		this.timeDiff = timeDiff;
	}
	
}
